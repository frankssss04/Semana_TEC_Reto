def square(x, y):
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()
    for count in range(4):
        path.forward(20)
        path.left(90)
    path.end_fill()


def offset(point):
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    index = offset(point)
    if tiles[index] == 0:
        return False
    index = offset(point + 19)
    if tiles[index] == 0:
        return False
    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    bgcolor('black')
    path.color('blue')
    for index in range(len(tiles)):
        tile = tiles[index]
        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)
            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    writer.undo()
    writer.write(state['score'])
    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)
    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course) and (point.x % 20 != 0 or point.y % 20 != 0):
            point.move(course)
        else:
            options = [vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)]
            valid_options = [o for o in options if valid(point + o)]
            reverse = vector(-course.x, -course.y)
            non_reverse = [o for o in valid_options if not (o.x == reverse.x and o.y == reverse.y)]
            choices = non_reverse if non_reverse else valid_options

            if choices:
                best = min(choices, key=lambda v: abs((point + v) - pacman))
                plan = best if random() > 0.2 else choice(choices)
                course.x, course.y = plan.x, plan.y
                point.move(course)

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            writer.goto(0, 0)
            writer.color('white')
            writer.write('GAME OVER', align='center', font=('Arial', 16, 'bold'))
            return

    # Fantasmas más rápidos (60 ms en lugar de 100)
    ontimer(move, 60)


def change(x, y):
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


setup(420, 460, 370, 0)
